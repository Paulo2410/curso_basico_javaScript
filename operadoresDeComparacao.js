// Operadores de comparação e expressões de comparação

console.log(10 > 5)                // Maior              true
console.log(7 < 5)                 // Menor              false
console.log(9 >= 9)                // Maior ou igual     true
console.log(18 <= 11)              // Menor ou igual     false
console.log("abacaxi" == "uva")    // Igual              false
console.log("bicicleta" != "joão") // Diferente          true